# 建立游戏名 → id 的映射
cur.execute("SELECT id, name FROM games")
name_to_id = {row[1]: row[0] for row in cur.fetchall()}
print(f"现有游戏: {len(name_to_id)} 款")

# --- 插入新游戏 ---
new_count = 0
for name, slug, desc, cat, date, dev, pub in NEW_GAMES:
    if name in name_to_id:
        continue
    cur.execute("""
        INSERT INTO games (name, slug, description, category, release_date, developer, publisher, cover_image, created_at)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
    """, (name, slug, desc, cat, date, dev, pub, f'/images/games/{slug}.jpg', now()))
    name_to_id[name] = cur.lastrowid
    new_count += 1
conn.commit()
print(f"新增游戏: {new_count} 款")
print(f"总游戏数: {len(name_to_id)} 款\n")

# --- 插入角色 ---
cur.execute("SELECT COUNT(*) FROM characters")
if cur.fetchone()[0] > 0:
    print(f"角色表已有数据，跳过角色插入")
else:
    char_count = 0
    for game_name, name, rarity, element, weapon, desc, image in CHARACTERS:
        gid = name_to_id.get(game_name)
        if not gid:
            print(f"  警告: 游戏 '{game_name}' 未找到，跳过角色 {name}")
            continue
        cur.execute("""
            INSERT INTO characters (game_id, name, rarity, element, weapon_type, description, image, created_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        """, (gid, name, rarity, element, weapon, desc, image, now()))
        char_count += 1
    conn.commit()
    print(f"新增角色: {char_count} 个\n")

# --- 插入文章 ---
cur.execute("SELECT slug FROM articles")
existing_slugs = {row[0] for row in cur.fetchall()}

article_count = 0
for game_name, title, slug, content, category, tags in ARTICLES:
    if slug in existing_slugs:
        continue
    gid = name_to_id.get(game_name) if game_name else None
    cur.execute("""
        INSERT INTO articles (title, slug, content, game_id, author_id, category, tags, views, likes, is_published, created_at)
        VALUES (?, ?, ?, ?, ?, ?, ?, 0, 0, 1, ?)
    """, (title, slug, content, gid, 1, category, tags, now()))
    existing_slugs.add(slug)
    article_count += 1

conn.commit()
print(f"新增文章: {article_count} 篇\n")

